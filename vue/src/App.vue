<template>
  <div id="app">
    <router-view/>
  </div>
</template>

<script>
export default {
  name: 'App',
  mounted () {
    window.addEventListener('unload', this.saveState)
    this.$store.commit('reset')
  },
  methods: {
    saveState () {
      localStorage.setItem('vuex', JSON.stringify(this.$store.state.userInfo))
    }
  }
}
</script>

